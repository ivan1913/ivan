package calculos;

public class operaciones {
	
	public static int suma(int n, int n2) {
		return n+n2;
	}
	
	public static int resta(int n, int n2) {
		return n-n2;
	}
	
	public static int multiplicacion(int n, int n2) {
		return n*n2;
	}
	
	public static int division(int n, int n2) {
		return n/n2;
	}

}
